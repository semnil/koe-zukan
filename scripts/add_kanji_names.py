def main():
    wb = openpyxl.load_workbook(DATA_FILE)
    ws = wb["名称マッピング"]

    # Build row index: ID → row number
    updated = 0
    skipped = 0
    for row in ws.iter_rows(min_row=2):
        aid = str(row[0].value) if row[0].value else ""
        if aid not in KANJI_MAP:
            continue

        kanji = KANJI_MAP[aid]
        current_alt = row[4].value or ""

        # Skip if kanji already present
        if kanji in current_alt:
            skipped += 1
            continue

        # Append kanji to existing altJA
        if current_alt:
            new_alt = f"{current_alt}, {kanji}"
        else:
            new_alt = kanji

        row[4].value = new_alt
        updated += 1
        print(f"  {aid}: {row[1].value} → altJA = {new_alt}")

    print(f"\nUpdated: {updated}, Skipped (already present): {skipped}")

    wb.save(DATA_FILE)
    print(f"Saved to {DATA_FILE}")


if __name__ == "__main__":
    main()
