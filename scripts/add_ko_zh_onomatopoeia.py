def main():
    wb = openpyxl.load_workbook(DATA_FILE)
    ws = wb["オノマトペマッピング"]

    # Collect existing ko/zh IDs
    existing_ko = set()
    existing_zh = set()
    max_row = ws.max_row
    for row in ws.iter_rows(min_row=2, values_only=True):
        if not row[0]:
            continue
        aid = str(row[0])
        lang = row[2] or ""
        if lang == "ko":
            existing_ko.add(aid)
        elif lang == "zh":
            existing_zh.add(aid)

    added_ko = 0
    added_zh = 0
    current_row = max_row + 1

    for aid, (ko_ono, zh_ono) in sorted(ONOMATOPOEIA_DATA.items()):
        if aid not in existing_ko and ko_ono:
            ws.cell(row=current_row, column=1, value=aid)
            ws.cell(row=current_row, column=3, value="ko")
            ws.cell(row=current_row, column=4, value="한국어")
            ws.cell(row=current_row, column=5, value=ko_ono)
            current_row += 1
            added_ko += 1

        if aid not in existing_zh and zh_ono:
            ws.cell(row=current_row, column=1, value=aid)
            ws.cell(row=current_row, column=3, value="zh")
            ws.cell(row=current_row, column=4, value="中文")
            ws.cell(row=current_row, column=5, value=zh_ono)
            current_row += 1
            added_zh += 1

    print(f"Added: ko={added_ko}, zh={added_zh}")
    print(f"Total new rows: {added_ko + added_zh}")

    wb.save(DATA_FILE)
    print(f"Saved to {DATA_FILE}")


if __name__ == "__main__":
    main()
